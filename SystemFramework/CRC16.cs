        /********************************************************************
                                          CRC16
                
        * 功 能: 16位CRC计算 -- CRC 循环冗余校验
        *
        * 参 数:@para1: unsigned char *puchMsg 要进行CRC校验的消息
        *       @para2: unsigned short usDataLen; 消息中字节数
        *
        * 返 回: 无
        *
        * 创 建: 吴兵   2015-06-03  
        *
        * 维 护: 戴杰   2016-01-06
        *
        * 其 他: 循环冗余校验CRC区为2字节，含一个16位二进制数据。由发送设备计算CRC值，并把计算值附在信息中，
                接收设备在接收信息时，重新计算CRC值，并把计算值与接收的在CRC区中实际值进行比较，若两者不相同，
                则产生一个错误。CRC开始时先把寄存器的16位全部置成"1",然后把相邻2个8位字节的数据放入当前寄存器中，
                只有每个字符的8位数据用作产生CRC，起始位，停止位和奇偶校验位不加到CRC中。产生CRC期间，每8位数据与
                寄存器中值进行异或运算，其结果向右移一位(向LSB方向)，并用"0"填入MSB，检测LSB，若LSB为"1"则与预置的
                固定值异或，若LSB为"0"则不作异或运算。
                重复上述处过程，直至移位8次，完成第8次移位后，下一个8位数据，与该寄存器的当前值异或，
                在所有信息处理完后，寄存器中的最终值为CRC值.
        ********************************************************************/
        /// <summary>
        /// CRC16校验法
        /// </summary>
        /// <param name="puchMsg">需要校验的byte[]</param>
        /// <returns></returns>
        public static string GetString(string str)
        {
            byte[] puchMsg = Encoding.UTF8.GetBytes(str);
            int uchCRCHi = 0xFF; /*高CRC字节初始化*/
            int uchCRCLo = 0xFF; /*低CRC字节初始化*/
            int usDataLen = puchMsg.Length;
            int uIndex; /*CRC循环中的索引*/

            for (int i = 0; i < usDataLen; i++)    /*传输消息缓冲区*/
            {
                uIndex = uchCRCHi ^ puchMsg[i]; /*计算CRC*/
                uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex];
                uchCRCLo = auchCRCLo[uIndex];
            }
            string crc = Convert.ToString(((uchCRCHi << 8) | uchCRCLo), 16);
            crc = crc.Length % 2 != 0 ? "0" + crc : crc;//补0
            crc = crc.Length == 2 ? "00" + crc : crc;//补全4位
            return crc;
        }
    }
